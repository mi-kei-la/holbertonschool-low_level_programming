#include "functions.h"

/**
* add - add two integers
*
* @a: parameter
* @b: parameter
*
* Return: result
*/

int add(int a, int b)
{
	return (a + b);
}

/**
* sub - substract two integers
*
* @a: parameter
* @b: parameter
*
* Return: result
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - multiply two integers
*
* @a: parameter
* @b: parameter
*
* Return: result
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
* div - divide two integers
*
* @a: parameter
* @b: parameter
*
* Return: result
*/

int div(int a, int b)
{
	return (a / b);
}

/**
* mod - module of two integers
*
* @a: parameter
* @b: parameter
*
* Return: result
*/

int mod(int a, int b)
{
	return (a % b);
}
